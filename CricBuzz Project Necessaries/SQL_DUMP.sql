-- ----------------------------
-- Function structure for SEARCH_A_PLAYER
-- ----------------------------
CREATE OR REPLACE
PROCEDURE "PDB"."SEARCH_A_PLAYER" AS
BEGIN

SELECT ROLE INTO P_ROLE FROM PLAYERS WHERE PLAYER_NAME = P_NAME;

IF P_ROLE = 'BATSMAN' THEN

SELECT PLAYER_NAME,BATTING_STYLE,BATTING_RANK,HUNDREDS,FIFTIES,BEST 
FROM BATSMANS
WHERE PLAYER_NAME = P_NAME;


ELSIF P_ROLE = 'BOWLER' THEN

SELECT PLAYER_NAME,BOWLING_STYLE,BOWLING_RANK,ECONOMY,WICKETS,BEST_FIGURE 
FROM BOWLERS
WHERE PLAYER_NAME = P_NAME;

ELSIF P_ROLE = 'ALLROUNDER' THEN

SELECT BT.PLAYER_NAME,BT.BATTING_STYLE,BT.BATTING_RANK,BT.HUNDREDS,BT.FIFTIES,BT.BEST,
BL.BOWLING_STYLE,BL.BOWLING_RANK,BL.ECONOMY,BL.WICKETS,BL.BEST_FIGURE 

FROM BATSMANS BT JOIN BOWLERS BL on(BT.PLAYER_ID = BL.PLAYER_ID)
WHERE BT.PLAYER_NAME = P_NAME;

ELSE 

DBMS_OUTPUT.PUT_LINE('NOT FOUND');

END IF;



END;
/

